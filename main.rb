Reflecting on Principle
1) How are Ruby modules (and similar features from other langs)
   different from a class, in their purpose?
   Modules are different than classes because they will never be instantiated. They are a collection of methods that classes can use to gain access to those methods
2) What is LSP?
	The Liskov Substitution Principle states that any subclass or subtype should be able to stand in for its superclass or supertype.
3) Briefly describe how the template method pattern works.
	Define a basic structure or function in the superclass, then messages are sent to the subclass(es) to get their subclass-specific contributions.
